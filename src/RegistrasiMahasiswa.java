    private void tballMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tballMouseClicked
        tfpendaftaran.setEnabled(false);
        
        int baris = tball.rowAtPoint(evt.getPoint());
        
        String ID = tball.getValueAt(baris, 0).toString();

        try {
            int no=1;
            String sql = "select * from tb_registrasi where id='"+ID+"'";
            java.sql.Connection conn=(Connection)database.configDB();
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet res=stm.executeQuery(sql);
            while(res.next()){
                tfID.setText(res.getString("id"));
                tfpendaftaran.setText(res.getString("numberregistration"));
                tfnama.setText(res.getString("fullname"));
                tflahir.setText(res.getString("placeborn"));
                cbkelamin.setSelectedItem(res.getString("gender"));
                cbagama.setSelectedItem(res.getString("religion"));
                cbdarah.setSelectedItem(res.getString("bloodgroup"));
                taalamat.setText(res.getString("address"));
                cbkabupaten.setSelectedItem(res.getString("city"));
                cbprovinsi.setSelectedItem(res.getString("province"));
                tfpos.setText(res.getString("zipcode"));
                tfsekolah.setText(res.getString("fromschool"));
                tflulus.setText(res.getString("graduationyear"));
                tfijazah.setText(res.getString("diplomanumber"));
                tfun.setText(res.getString("testscores"));
                tfhp.setText(res.getString("telp"));
                tfemail.setText(res.getString("email"));
                cbjurusan.setSelectedItem(res.getString("choosemajors"));
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_tballMouseClicked

