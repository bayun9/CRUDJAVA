    private void load_table(){
        tfpendaftaran.setEnabled(true);
        
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No");
        model.addColumn("No Pendaftaran");
        model.addColumn("Nama");
        model.addColumn("Asal Sekolah");
        model.addColumn("Tahun Lulus");
        model.addColumn("Telp / HP");
        model.addColumn("Jurusan");
        
        //menampilkan data database kedalam tabel
        try {
            int no=1;
            String sql = "select * from tb_registrasi";
            java.sql.Connection conn=(Connection)database.configDB();
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet res=stm.executeQuery(sql);
            while(res.next()){
                model.addRow(new Object[]{no++,
                    res.getString(2),
                    res.getString(3),
                    res.getString(12),
                    res.getString(13),
                    res.getString(16),
                    res.getString(18)});
            }
            tball.setModel(model);
        } catch (Exception e) {
        }
    }
    
